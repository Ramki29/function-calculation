	let sub=b-c;
	document.write(sub);
	document.write("<br>");
	let multi=b*c;
	document.write(multi);
	document.write("<br>");
	let div=b/c;
	document.write(div);
	document.write("<br>");
	let modulo=b%c;
	document.write(modulo);
